def classify_slit_pattern(st_list, venue_settings):
    """
    st_list: [st1, st2, st3, st4, st5, st6]
    venue_settings:
        - lead_threshold
        - big_delay_threshold
    """

    lead_th = venue_settings["lead_threshold"]
    delay_th = venue_settings["big_delay_threshold"]

    # 平均STとの差
    avg_st = sum(st_list) / 6
    st_diff = [st - avg_st for st in st_list]

    # STの速い順に並べたインデックス
    order = sorted(range(6), key=lambda i: st_list[i])

    # ---------------------------------------------------------
    # 2. 横一線（最速と最遅の差が 0.5 以内）
    # ---------------------------------------------------------
    if max(st_list) - min(st_list) <= 0.5:
        return 2

    # ---------------------------------------------------------
    # 3. 1・2先行（order[0]=1号艇, order[1]=2号艇）
    # ---------------------------------------------------------
    if order[0] == 0 and order[1] == 1:
        return 3

    # ---------------------------------------------------------
    # 4. スロー先行（1〜3が上位3つ）
    # ---------------------------------------------------------
    if set(order[:3]).issubset({0, 1, 2}):
        return 4

    # ---------------------------------------------------------
    # 5. カベなし（2号艇が遅れる）
    # ---------------------------------------------------------
    if st_diff[1] >= delay_th:
        return 5

    # ---------------------------------------------------------
    # 6. 2・3遅れ
    # ---------------------------------------------------------
    if st_diff[1] >= delay_th and st_diff[2] >= delay_th:
        return 6

    # ---------------------------------------------------------
    # 7. 中凹み（3・4遅れ）
    # ---------------------------------------------------------
    if st_diff[2] >= delay_th and st_diff[3] >= delay_th:
        return 7

    # ---------------------------------------------------------
    # 8. 3の先攻め（3号艇が最速）
    # ---------------------------------------------------------
    if order[0] == 2:
        return 8

    # ---------------------------------------------------------
    # 9. 中ぶくれ（3・4が先行）
    # ---------------------------------------------------------
    if set(order[:2]) == {2, 3}:
        return 9

    # ---------------------------------------------------------
    # 10. 1が遅れる
    # ---------------------------------------------------------
    if st_diff[0] >= delay_th:
        return 10

    # ---------------------------------------------------------
    # 11. 外側先行（4〜6が先行）
    # ---------------------------------------------------------
    if order[0] in {3, 4, 5} and order[1] in {3, 4, 5}:
        return 11

    # ---------------------------------------------------------
    # 12. ダッシュ先行（4〜6が上位3つ）
    # ---------------------------------------------------------
    if set(order[:3]).issubset({3, 4, 5}):
        return 12

    # ---------------------------------------------------------
    # 1. 内側先行（フォールバック）
    # ---------------------------------------------------------
    return 1
