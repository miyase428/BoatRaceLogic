# baseline.py
# ---------------------------------------------
# 基礎力スコア（baseline）を計算するモジュール
# get_input_data.php の entries[] の1行を入力として受け取る
# ---------------------------------------------

def calc_baseline(entry: dict) -> float:
    """
    entry: get_input_data.php が返す entries[] の1行
    """

    # ---------------------------------------------
    # ① 全国勝率（0〜10 → 0〜1）
    # ---------------------------------------------
    win_rate = float(entry.get("national_win_rate", 0) or 0)
    win = win_rate / 10

    # ---------------------------------------------
    # ② 当地勝率（0〜10 → 0〜1）
    # ---------------------------------------------
    local_rate = float(entry.get("local_win_rate", 0) or 0)
    local = local_rate / 10

    # ---------------------------------------------
    # ③ モーター2連率（0〜50 → 0〜1）
    # ---------------------------------------------
    motor_rate = float(entry.get("motor_exacta_rate", 0) or 0)
    motor = motor_rate / 50

    # ---------------------------------------------
    # ④ ボート2連率（0〜50 → 0〜1）
    # ---------------------------------------------
    boat_rate = float(entry.get("boat_exacta_rate", 0) or 0)
    boat = boat_rate / 50

    # ---------------------------------------------
    # ⑤ 地力（勝率＋当地）
    # ---------------------------------------------
    jiryoku = (win + local) / 2

    # ---------------------------------------------
    # ⑥ 足（モーター＋ボート）
    # ---------------------------------------------
    ashi = (motor + boat) / 2

    # ---------------------------------------------
    # ⑦ baseline = 地力70% + 足30%
    # ---------------------------------------------
    baseline = 0.7 * jiryoku + 0.3 * ashi

    return baseline
